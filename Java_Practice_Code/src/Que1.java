
/*
 * Q.1 Swap 2 variable without using 3rd variable
 */
public class Que1 {

	public static void main(String[] args) {
	
		int a = 10;
		int b = 30;
		
		int c;
		
		
		
		System.out.println("A: "+ a);
		System.out.println("B: "+ b);
		
		System.out.println("----------After swapping---------");
		
		c = a;
		a = b;
		b = c;
		
		System.out.println("A: "+ a);
		System.out.println("B: "+ b);
	}

}
